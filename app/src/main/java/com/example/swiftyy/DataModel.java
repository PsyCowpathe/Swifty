package com.example.swiftyy;

public class DataModel
{
    String name;
    String mark;

    public DataModel(String name, String mark)
    {
        this.name = name;
        this.mark = mark;
    }

    public String getName()
    {
        return (name);
    }

    public String getMark()
    {
        return (mark);
    }
}
